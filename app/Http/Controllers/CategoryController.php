                return Category::all()->map(function ($cat) {
                    return $cat->only('name', 'id');
                });
            }
        ]);
    }
    /**
     * toggle attaching categories in the geveen post.
     *
     * @return \Illuminate\Http\Response
     */
    public function toggel_post(Post $post, Request $request)
    {
        if (is_int($request->cat)) {
            $post->categories()->toggle($request->cat);
        }
        return back(303);
    }

